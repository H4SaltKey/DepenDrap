function tryAdvanceFirstDrawToPlayingIfBothReady() {
    // [追加] Firebase状態の最新値を強制取得
    await new Promise(resolve => setTimeout(resolve, 500)); // 同期待機
  const m = state.matchData;

    if (!m || m.status !== "setup_first_draw") return;



























    // [修正] ready判定ロジック強化
    const bothReady = m.players.every(p => p.readyState === 'ready');
    if (!bothReady) {
        traceGame("phaseTransition", "waiting", "players not ready");
        return;
    }
































    // [追加] 二重遷移防止チェック
    if (window.__playingStarted) return;




    // [修正] UI更新と状態更新の順序制御
    updateMatchUI(); // 先にUI更新
    await updateFirebaseStatus('playing'); // その後状態更新



































































































































    window.__playingStarted = true;
    window._firstDrawAdvanceSent = true;
    traceGame("phaseTransition", "complete", "setup_first_draw -> playing");
}
























































































































































































































